    private void XoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_XoaActionPerformed
        // TODO add your handling code here:
        // Lấy mặt hàng được chọn từ danh sách
        int selectedIndex = tblMatHang.getSelectedRow();
        
        if (selectedIndex >= 0) {
        // Lấy mã mặt hàng từ dòng được chọn
            String maMH = tblMatHang.getValueAt(selectedIndex, 3).toString(); // Giả sử cột 3 là cột chứa MAMH
            model.removeRow(selectedIndex);
            
        // Xóa mặt hàng từ cơ sở dữ liệu
            if (new DAO().deleteMatHang(maMH)) {
            
                JOptionPane.showMessageDialog(rootPane, "Delete Success!");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Delete Failed!");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Select a row to delete!");
        }
    }//GEN-LAST:event_XoaActionPerformed

