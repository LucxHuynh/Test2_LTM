    private void fillComboBox() {
        try {
            // Kết nối đến cơ sở dữ liệu (thay đổi thông tin kết nối tùy theo cấu hình của bạn)
            Connection connection = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=VANPHONGPHAM;user=sa;password=huynhtanloc_0x");

            // Thực hiện truy vấn để lấy dữ liệu từ cơ sở dữ liệu
            
                String query = "SELECT * FROM LOAI_MAT_HANG";
            try (PreparedStatement statement = connection.prepareStatement(query);
                 ResultSet resultSet = statement.executeQuery()) {

                // Đổ dữ liệu vào ComboBox
                while (resultSet.next()) {
                    String item = resultSet.getString("MALOAI");
                    cmbMatHang.addItem(item);
                }
                
            }
            // dong ket noi
            connection.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

